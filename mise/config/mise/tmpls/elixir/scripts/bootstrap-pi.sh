#!/usr/bin/env bash
# Generate AGENTS.md (project/worktree root) and merge the tidewave MCP server
# into .pi/mcp.json. Run by `mise run bootstrap:pi`. Resolves the dev port
# directly via the copied .config/scripts/phx-port.sh.
set -euo pipefail
mkdir -p .pi
port="$(bash .config/scripts/phx-port.sh)"

# ‚îÄ‚îÄ AGENTS.md (source of truth is this inline markdown) ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
# Written to the project/worktree root (git toplevel), not .pi/agent/.
root="$(git rev-parse --show-toplevel 2>/dev/null || pwd)"
f="$root/AGENTS.md"
[ -L "$f" ] && rm "$f"
# clean up old generated location
[ -e .pi/agent/AGENTS.md ] && rm .pi/agent/AGENTS.md
cat >"$f" <<'AGENTS'
# Agent guide (generated by `mise run bootstrap:pi`)

Elixir Phoenix LiveView application.
Local tooling is managed by mise (`.config/mise.toml`).

## Development requirements

- All code introspection, editing, reading, REPL usage, database access; should
  use the `tidewave` mcp server, see .pi/mcp.json. All attempts should be made
  to use that tooling first, before resorting to other Bash, Read, and/or Write tool calls.

## Testing requirements

- If postgres connection related errors occur during test running, pass `POOL_SIZE` env var
  value to reduce the pool size to something more manageable for this local machine.
- Whether we caused a test error, in the current commit, branch, PR, or not, we absolutely
  must make all attempts at correctly fixing the errors.

## Ports

- Phoenix dev port derives from `GIT_WORKTREE`:
  `4000 + :erlang.phash2(GIT_WORKTREE, 1000)` (see `config/dev.exs`).
- `PHX_PORT` is exported by mise via `.config/scripts/phx-port.sh`:
  `$PORT` override ‚Üí running beam process ‚Üí deterministic phash fallback.
- Tidewave MCP server: `http://localhost:$PHX_PORT/tidewave/mcp`
  (wired into `.pi/mcp.json`; requires the Phoenix server running in dev).

## Tasks

- `mise run preflight` ‚Äî trust config, install tools, bootstrap, start shared postgres
- `mise run setup` ‚Äî full app setup (hex, deps, assets, databases)
- `mise run start:server` ‚Äî start Phoenix (`m s`)
- `mise run bootstrap` ‚Äî regenerate this file (AGENTS.md) and `.pi/mcp.json`

## Verification

- `mix format` after every major change
- `mix test` after every work session
- `mix check` before committing
AGENTS

# ‚îÄ‚îÄ .pi/mcp.json: merge tidewave entry, preserve other servers ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
m=.pi/mcp.json
[ -L "$m" ] && rm "$m"
[ -s "$m" ] || echo '{"mcpServers":{}}' >"$m"
tmp="$(mktemp)"
jq --arg url "http://localhost:${port}/tidewave/mcp" \
  '.mcpServers.tidewave = {type: "streamable-http", url: $url, lifecycle: "keep-alive"}' \
  "$m" >"$tmp" && mv "$tmp" "$m"

echo "bootstrapped .pi (tidewave -> http://localhost:${port}/tidewave/mcp)"
